	 AREA RESET, DATA, READONLY
	 EXPORT __Vectors
	 
__Vectors
	DCD 0x10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R1,=SRC
	LDR R2,=DST
	MOV R3,#0X0
	MOV R4,#0X0
	MOV R5,#10
	ADD R3,R4
LOOP
	LDR R7,[R1],#4
	ADC R3,R7
	SUBS R5,#1
	BNE LOOP
	
	STR R3,[R2]
STOP
	B STOP
SRC DCD 0x11111111, 0x22222222, 0x33333333,0x44444444, 0x55555555, 0x66666666, 0x77777777, 0x88888888, 0x99999999, 0x12345678
	AREA DATA_SEG, DATA, READWRITE
DST DCD 0 
	END