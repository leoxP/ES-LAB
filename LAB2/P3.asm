	 AREA RESET, DATA, READONLY
	 EXPORT __Vectors
	 
__Vectors
	DCD 0x10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R0,=NUM1+12
	LDR R1,=NUM2+12
	LDR R2,=DST+12
	
	MOV R6,#0
	MOV R7,#0
	ADD R6,R7
	MOV R6,#4
	
LOOP
	LDR R3,[R0],#-4
	LDR R4,[R1],#-4
	
	SBC R5,R3,R4
	STR R5,[R2],#-4
	
	SUBS R6,#1
	BNE LOOP
	
STOP
	B STOP
NUM2 DCD 0x11111111,0X22222222,0X33333333,0X44444444
NUM1 DCD 0X55555555,0X66666666,0X77777777,0X88888888
	AREA DATA_SEG, DATA, READWRITE
DST DCD 0 
	END