	 AREA RESET, DATA, READONLY
	 EXPORT __Vectors
	 
__Vectors
	DCD 0x10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R2,=NUM1
	LDR R3,=NUM2
	LDR R0,[R2]
	LDR R1,[R3]
	
	LDR R7,=RES

LOOP
	CMP R0,R1
	BEQ OUT
	BHI GREATER
	BLS LESSER
GREATER
	SUB R0,R1
	B LOOP
LESSER
	SUB R1,R0
	B LOOP

OUT
	STR R1,[R7]
	
STOP
	B STOP
NUM1 DCD 0X06
NUM2 DCD 0X0A
	AREA DATA_SEG, DATA, READWRITE
RES DCD 0 
	END