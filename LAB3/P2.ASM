	AREA RESET, DATA, READONLY
	 EXPORT __Vectors
	 
__Vectors
	DCD 0x10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R6,=BCD
	LDR R7,=HEX
	LDR R0,[R6]
	AND R1,R0,#0X0F
	AND R2,R0,#0XF0
	LSR R2,#04
	MOV R3,#0X0A
	MUL R4,R2,R3
	ADD R4,R1
	STR R4,[R7]
STOP
	B STOP
BCD DCD 0X34
	AREA data, DATA, READWRITE
HEX DCD 0