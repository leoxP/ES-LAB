	AREA RESET, DATA, READONLY
	 EXPORT __Vectors
	 
__Vectors
	DCD 0x10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R0,=NUM
	LDR R4,=RESULT
	LDRB R1,[R0]
	AND R2,R1,#0x0F
	CMP R2,#09 
	BLO DOWN 
	ADD R2,#07
DOWN
	ADD R2,#0x30
	STRB R2,[R4] 
	AND R3,R1,#0xF0 
	MOV R3,R3,LSR#04 
	CMP R3,#09 
	BLO DOWN1
	ADD R3,#07
DOWN1
	ADD R3,#0x30 
	STRB R3,[R4,#1]
STOP
	B STOP
NUM DCD 0x000003A
	AREA data, DATA, READWRITE
RESULT DCD 0