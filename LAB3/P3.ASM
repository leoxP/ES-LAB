	AREA RESET, DATA, READONLY
	 EXPORT __Vectors
	 
__Vectors
	DCD 0x10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R6,=HEX
	LDR R7,=BCD
	LDRB R0,[R6]
	MOV R2,#0XA
	MOV R1,#0
LOOP
	SUBS R0,R0,R2
	BMI EXIT
	ADD R1,#1
	B LOOP
EXIT
	LSL R1,4
	ADD R0,R2
	ADD R1,R0
	STR R1,[R7]
STOP
	B STOP
HEX DCD 0x2A
	AREA data, DATA, READWRITE
BCD DCD 0